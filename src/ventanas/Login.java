    private void Button_AccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_AccederActionPerformed

        email = txt_user.getText().trim();
        pass = txt_password.getText().trim();

        if (!email.equals("") || !pass.equals("")) {

            try {

                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement("select permissions from Users where email = '" + email
                        + "' and password = '" + pass + "'");

                ResultSet rs = pst.executeQuery();

                if (rs.next()) {

                    System.out.print("rs vale: " + rs);

                    String status = rs.getString("permissions");

                    if (status.equalsIgnoreCase("admin")) {
                        //Este metodo permite que el JFrame sea destruido
                        //y se ejecute lo siguiente
                        dispose();
                        new Administrador().setVisible(true);

                    } else {
                        JOptionPane.showMessageDialog(null, "Datos de acceso incorrectos");
                        txt_user.setText("");
                        txt_password.setText("");

                    }
                }
            } catch (SQLException e) {

                System.err.println("Error en el boton acceder ");
                JOptionPane.showMessageDialog(null, "Â¡Error al iniciar sesion!\n"
                        + "comunicate con el administrador");

            }

        } else {
            JOptionPane.showMessageDialog(null, "Tienes que llenar los campos");

        }


    }//GEN-LAST:event_Button_AccederActionPerformed

