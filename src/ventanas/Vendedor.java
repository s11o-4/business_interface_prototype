    private void JButton_CrealoTu1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButton_CrealoTu1ActionPerformed
         
       Document documento = new Document();
       
       try{
           
           //Este metodo te ayuda a conseguir la direccion de el usuario
           //para posteriormente poder decidir donde lo quieres guardar
           String ruta = System.getProperty("user.home");
           
           //Aqui solo estoy indicando en donde quiero que se guarde mi archivo
           //con la variable String consigo la ruita de mi user y con la clase padWriter otneog el
           //metodo para crear una instancia y despues indicar donde quiero que se guarde el documento.
           PdfWriter.getInstance(documento, new FileOutputStream(ruta + "/Desktop/Reporte De Ventas.pdf"));
           
           documento.open();
           
           PdfPTable tabla = new PdfPTable(7);
           
           tabla.addCell("Nombre del Comprador");
           tabla.addCell("Telefono");
           tabla.addCell("Correo");
           tabla.addCell("Precio vendido del Amplificador");
           tabla.addCell("Tipo de AmpÄºificador");
           tabla.addCell("Wattts");
           tabla.addCell("Fecha");
           
           
           try{
           
               Connection cn = Conexion.conectar();
               PreparedStatement pst = cn.prepareStatement("select * from ventas");
               
               ResultSet rs = pst.executeQuery();
               
               if(rs.next()){
                   
                   do{
                      
                       //El salto de fila lo hace automaticamente
                       //mi clase tabla
                       
                       //No te confundas aqui dice agregar celda haciendo referencia a cada espacio
                       //generado
                       tabla.addCell(rs.getString(1));
                       tabla.addCell(rs.getString(2));
                       tabla.addCell(rs.getString(3));
                       tabla.addCell(rs.getString(4));
                       tabla.addCell(rs.getString(5));
                       tabla.addCell(rs.getString(6));
                       tabla.addCell(rs.getString(7));
                       
                       
                       
                   } while(rs.next());
                     
                    documento.add(tabla);
                   
               }
               
               
           } catch(Exception e){
               
           }
           
           documento.close();
           JOptionPane.showMessageDialog(null, "Reporte de ventas creado ");
           
       } catch(Exception e){
           
       }
        
        
        
        
    }//GEN-LAST:event_JButton_CrealoTu1ActionPerformed

